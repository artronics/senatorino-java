package com.artronics.embedded.register;

public class RegisterBit
{
    private String name;
    private int mask;

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public int getMask()
    {
        return mask;
    }

    public void setMask(int mask)
    {
        this.mask = mask;
    }
}
